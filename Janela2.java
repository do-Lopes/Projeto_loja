    public void listarTabela(){
        
        DefaultTableModel modelo = (DefaultTableModel)TabCam.getModel();
        modelo.setRowCount(0);
        int posLin = 0;
        
        for(Camisa c1 : Cam.getBdCam()){
            modelo.insertRow(posLin, new Object[]{c1.getMarca(),c1.getGen(),c1.getTaman(),c1.getCor(),c1.getMate(),c1.getEstampa(),c1.getModelo(),c1.getGola(),c1.getPreco(),c1.getCusto(c1.getMate())});
            posLin++;
        }
    }
    
    public void modificar(){
        c1 = new Camisa();
        
        c1.setCod(Integer.parseInt(txtCodigo.getText()));
        
        c1 = Cam.AttCamCod(c1);
        
        if(c1 != null){
            JOptionPane.showMessageDialog(null, "Roupa Atualizada com sucesso", "Atualização de dados",0);
                txtPreco.setText(Integer.toString(c1.getPreco()));
                txtMarca.setText(c1.getMarca());
                txtPublico.setText(c1.getGen());
                txtTamanho.setText(c1.getTaman());
                txtCor.setText(c1.getCor());
                txtMaterial.setText(c1.getMate());
                txtEstampa.setText(c1.getEstampa());
                txtModelo.setText(c1.getModelo());
                txtGola.setText(c1.getGola()); 
        }else{
            JOptionPane.showMessageDialog(null, "Não existe uma roupa com o codigo "+txtCodigo.getText(), "Erro de consulta",0);
            limpar();
        }
    }
    
