    public void consultar(){
        c1 = new Camisa();
        
        c1.setCod(Integer.parseInt(txtCodigo.getText()));
        
        c1 = Cam.consCamCod(c1);
            
            if(c1 == null){
                JOptionPane.showMessageDialog(null, "Não existe uma roupa com o codigo "+txtCodigo.getText(), "Erro de consulta",0);
            limpar();
            }
            else{
                txtPreco.setText(Integer.toString(c1.getPreco()));
                txtMarca.setText(c1.getMarca());
                txtPublico.setText(c1.getGen());
                txtTamanho.setText(c1.getTaman());
                txtCor.setText(c1.getCor());
                txtMaterial.setText(c1.getMate());
                txtEstampa.setText(c1.getEstampa());
                txtModelo.setText(c1.getModelo());
                txtGola.setText(c1.getGola()); 
            }
    }
    
