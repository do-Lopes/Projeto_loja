    public void cadastrar(){
        c1 = new Camisa();
        
        try{
            c1.setPreco(Integer.parseInt(txtPreco.getText()));
        }
        catch(NumbNuloException nne){
            nne.NumbNulo();
            c1 = nne.corrNumb(c1);
	}
        catch(NumberFormatException nfe){
            FormatException x = new FormatException();
            c1 = x.corrForm(c1);
        }
        try{
            c1.setTaman(txtTamanho.getText());
	}
        catch(TamanRoupException tre){
            tre.TamanRoup();
            c1 = tre.corrTamn(c1);
	}
        try{
	c1.setMate(txtMaterial.getText());
	}
	catch(MateRoupaException mre){
            mre.MateRoupa();
            c1 = mre.corrMat(c1);	
	}
        try{
           c1.setGen(txtPublico.getText());
	}
	catch(GenException ge){
            ge.GenEx();
            c1 = ge.corrGen(c1);
	}
        try{
	c1.setEstampa(txtEstampa.getText());
	}
	catch(VerdadException ece){
            ece.Verdad();
            c1 = ece.corrOpc(c1);
	}
        try{
	c1.setModelo(txtModelo.getText());
	}
	catch(ModeloException mce){
            mce.Modelo();
            c1 = mce.corrMod(c1);	
	}
        try{
	c1.setGola(txtGola.getText());
	}
	catch(GolaCamiException gce){
            gce.ModeloGola();
            c1 = gce.corrGola(c1);
	}

            c1.setMarca(txtMarca.getText());
            c1.setCor(txtCor.getText());
            c1.setCod(Integer.parseInt(txtCodigo.getText()));
            c1.getCusto(c1.getMate());
            
            c1 = Cam.cadCam(c1);
            
            if(c1 == null){
                JOptionPane.showMessageDialog(null, "Ja existe uma roupa com esse codigo", "Erro de cadastro",0);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roupa cadastrada com sucesso","Cadastro OK", 0);
            }
    }
    
